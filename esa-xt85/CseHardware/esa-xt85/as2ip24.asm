cpu "8085.tbl"
hof "int8"

org 9000h

GTHEX: EQU 030EH
OUTPUT:EQU 0389H

CURDT: EQU 8FF1H
UPDDT: EQU 044cH
CURAD: EQU 8FEFH
UPDAD: EQU 0440H

MVI A,00H
MVI B,00H


LXI H,0000H
SHLD 8840H
SHLD 8842H

LXI H,8840H
CALL OUTPUT

MVI A,00H
MVI B,00H
CALL GTHEX ; Taking input into D-E pair
MOV H,D
MOV L,E

;Checking for valid input
FIRST:
	MOV A,H
	CPI 24H
	JC SECOND 
	
JMP TIMER

SECOND:
	MOV A,L
	CPI 60H
	SHLD CURAD
	MVI A,99H
	STA CURDT
	JC NXT_SEC
	
JMP TIMER

TIMER:
	LXI H, 0000H
	MVI A, 99H 
	SHLD CURAD
	STA CURDT



NXT_SEC:
	LHLD CURAD
	LDA CURDT
	INR A
	DAA
	CPI 60H
	JNZ ONE
		MVI A, 00H
		STA CURDT
		MOV A, L
		INR A
		DAA
		MOV L, A
		CPI 60H
		JNZ TWO
			MVI L, 00H
			MOV A, H
			INR A
			DAA
			MOV H, A
			CPI 24H
			JZ TIMER
			JMP TWO

	ONE:
	STA CURDT
	TWO:
	SHLD CURAD
	CALL UPDDT
	CALL UPDAD
	CALL DELAY
	JMP NXT_SEC


JMP OVER

DELAY:
	LXI H, 5D0FH
	LXI D, 0004H
	LOOP1:
		LOOP2:
			LOOP3:
				DCR L
				JNZ LOOP3
			DCR H
			JNZ LOOP2
		DCR E
		JNZ LOOP1
	RET

OVER: 
RST 5
cpu "8085.tbl"
hof "int8"

org 9000h

CURDT: EQU 8FF1H
UPDDT: EQU 044cH
CURAD: EQU 8FEFH
UPDAD: EQU 0440H
OUTPUT:EQU 0389H


TIMER:
	LXI H, 0000H
	MVI A, 99H 
	SHLD CURAD
	STA CURDT

NXT_SEC:
	LHLD CURAD
	LDA CURDT
	INR A
	DAA
	MVI B,60H
	CMP B
	JNZ ONE
		MVI A, 00H
		STA CURDT
		MOV A, L
		INR A
		DAA
		MOV L, A
		MVI B,60H
		CMP B
		JNZ TWO
			MVI L, 00H
			MOV A, H
			INR A
			DAA
			MOV H, A
			MVI B,12H
			CMP B
			JZ TIMER
			JMP TWO

	ONE:
	STA CURDT
	TWO:
	SHLD CURAD

	LDA 8840H
	MOV B,A
	LDA CURDT
	CMP B
	JNZ THREE
	LHLD CURAD
	LDA 8841H
	CMP L
	JNZ THREE
	LDA 8842H
	CMP H
	JNZ THREE

	JMP TIMER

	THREE: 
	CALL UPDDT
	CALL UPDAD
	CALL DELAY
	JMP NXT_SEC


JMP OVER

DELAY:
	LXI H, 540FH
	LXI D, 0004H
	LOOP1:
		LOOP2:
			LOOP3:
				DCR L
				JNZ LOOP3
			DCR H
			JNZ LOOP2
		DCR E
		JNZ LOOP1
	RET

OVER: 
RST 5
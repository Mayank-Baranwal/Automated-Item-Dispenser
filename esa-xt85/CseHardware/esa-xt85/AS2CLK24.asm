cpu "8085.tbl"
hof "int8"

org 9000h

CURDT: EQU 8FF1H
UPDDT: EQU 044cH
CURAD: EQU 8FEFH
UPDAD: EQU 0440H

TIMER:
	LXI H, 0000H
	MVI A, 99H 
	SHLD CURAD
	STA CURDT

NXT_SEC:
	LHLD CURAD
	LDA CURDT
	INR A
	DAA
	CPI 60H
	JNZ ONE
		MVI A, 00H
		STA CURDT
		MOV A, L
		INR A
		DAA
		MOV L, A
		CPI 60H
		JNZ TWO
			MVI L, 00H
			MOV A, H
			INR A
			DAA
			MOV H, A
			CPI 24H
			JZ TIMER
			JMP TWO

	ONE:
	STA CURDT
	TWO:
	SHLD CURAD
	CALL UPDDT
	CALL UPDAD
	CALL DELAY
	JMP NXT_SEC


JMP OVER

DELAY:
	LXI H, 5D0FH
	LXI D, 0004H
	LOOP1:
		LOOP2:
			LOOP3:
				DCR L
				JNZ LOOP3
			DCR H
			JNZ LOOP2
		DCR E
		JNZ LOOP1
	RET

OVER: 
RST 5